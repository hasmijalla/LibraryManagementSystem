	// when a record in the check-in table is clicked
	private void checkIntableMouseClicked(java.awt.event.MouseEvent evt) {
		int row = checkIntable.getSelectedRow();
		// to store the data in a Default table model vector
		DefaultTableModel defTableModel = (DefaultTableModel) checkIntable.getModel();
		LoanIdClicked = (int) defTableModel.getValueAt(row, 0);
		CardNoClicked = defTableModel.getValueAt(row, 1).toString();
		ISBNClicked = defTableModel.getValueAt(row, 2).toString();
	}

