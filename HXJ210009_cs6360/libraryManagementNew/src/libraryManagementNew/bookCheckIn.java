	// when checkIn button is clicked
	private void checkInBtnActionPerformed(java.awt.event.ActionEvent evt) {
		if (LoanIdClicked == 0) {
			JOptionPane.showMessageDialog(null, "Please select a record to Check-In");
		} else if (LoanIdClicked != 0) {
			try {
				Connection connect = DBConnection.connect();
				Statement st = connect.createStatement();
				String query = "update BOOK set is_available=1 where isbn='" + ISBNClicked + "';";
				int res1 = st.executeUpdate(query);
				String query1 = "update book_loans set date_in=CURDATE() WHERE loan_id=" + LoanIdClicked + ";";
				int res2 = st.executeUpdate(query1);
				if (res1 == 1 && res2 == 1) {
					JOptionPane.showMessageDialog(null, "Book Check-In Completed!");

					checkIntable.setVisible(true);
					String inputString = checkInInput.getText();
					try {
						query = "select  bl.LOAN_ID,bl.Card_NO,bl.ISBN, b.last_name from book_loans bl left join borrower b on bl.CARD_no=b.Card_no where (bl.Card_no like '%"
								+ inputString + "%' or bl.isbn like '%" + inputString + "%' or b.last_name like '%" + inputString
								+ "%') and (bl.date_in is null);";
						ResultSet resSet = st.executeQuery(query);
						checkIntable.setModel(DbUtils.resultSetToTableModel(resSet));
						resSet.close();
					} catch (Exception e) {
						e.printStackTrace();
					}
				
				} else {
					JOptionPane.showMessageDialog(null, "Check-In Failed.");
				}
			} catch (Exception e) {
				e.printStackTrace();
			}
		}
	}

